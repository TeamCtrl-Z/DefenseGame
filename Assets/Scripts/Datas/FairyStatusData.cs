/// <summary>
/// Fairy의 Status 데이터 클래스
/// </summary>
public class FairyStatusData
{
    /// <summary>
    /// Fairy의 ID
    /// </summary>
    public int FID;

    /// <summary>
    /// Fairy의 HP
    /// </summary>
    public float HP;

    /// <summary>
    /// Fairy의 공격력
    /// </summary>
    public float AttackPower;

    /// <summary>
    /// Fairy의 공격 속도
    /// </summary>
    public float AttackSpeed;

    /// <summary>
    /// Fairy의 우선 공격 대상
    /// </summary>
    public int Target;
}
